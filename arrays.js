//the Push Method will add and element to the end of the array
var Sports = ["Soccer", "Baseball", "Football", "Track and Field"]
Sports.push()
console.log()

//The Shift Method Removes the first element
var Ages = ["10", "12", "15", "17"]
Ages.shift()
console.log()

//The Unshift Method adds elements to the beggining of The array
var Registrationfee = ["true", "false", "false", "true"]
Registrationfee.unshift()
console.log()

//the index position is 0 also known as the first postion in the array.
//the index positions are as follows Sports - "Soccer", Ages - "10", and Registrationfee is "true"
